document.querySelectorAll("area").forEach(area => {
  const previewBox = document.getElementById("character-preview");
  const previewImg = document.getElementById("preview-img");

area.addEventListener("mouseenter", () => {
  const title = area.title;
  const data = characterImages[title];

  if (data) {
    previewImg.src = data.src;

    previewBox.style.top = data.top || "50px";
    previewBox.style.left = data.left || "1050px";
    previewImg.style.width = data.width || "auto";
    previewImg.style.height = "auto";

    previewBox.classList.add("show");
  }
});

area.addEventListener("mouseleave", () => {
  previewBox.classList.remove("show");
  previewImg.src = ""; // prevenir imagen "vieja" quedando en fadeout
  previewImg.style.width = "";
  previewImg.style.height = "";
});

});

window.addEventListener("DOMContentLoaded", () => {
  document.getElementById("page-wrapper").classList.add("fade-in");
});

